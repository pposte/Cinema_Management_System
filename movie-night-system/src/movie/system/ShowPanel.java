    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(jTextField1.getText().trim().compareTo("") == 0 || jTextField2.getText().trim().compareTo("") == 0 || jComboBox1.getSelectedItem() == null || jComboBox2.getSelectedItem() == null) return;
        String time = jTextField1.getText();
        Float price = Float.parseFloat(jTextField2.getText());
        int result = DBConnection.addShow(time, price);
        if(result > 0){
            jList1.setListData(DBConnection.getShows());
            JOptionPane.showMessageDialog(jComboBox1, "Show added!");
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't add the show!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(edit == false) return;
        String time = jTextField1.getText();
        Float price = Float.parseFloat(jTextField2.getText());
        int result = DBConnection.editShow(time, price);
        if(result > 0){
            JOptionPane.showMessageDialog(jComboBox1, "Show edited!");
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't edit the show!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

