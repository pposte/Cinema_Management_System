    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if(jList1.getSelectedValue() == null) return;
        Showing s = (Showing)jList1.getSelectedValue();
        DBConnection.showingId = s.getId();
        int result = DBConnection.deleteShow();
        if(result > 0){
            jList1.setListData(DBConnection.getShows());
            jTextField1.setText("");
            jTextField2.setText("");
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(DBConnection.getRooms()));
            jComboBox1.setSelectedIndex(-1);
            jComboBox2.setModel(new javax.swing.DefaultComboBoxModel(DBConnection.getMovies()));
            jComboBox2.setSelectedIndex(-1);
            edit = false;
            JOptionPane.showMessageDialog(jComboBox1, "Show deleted!");
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't delete the show!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        if(jList1.getSelectedValue() == null) return;
        edit = true;
        Showing s = ((Showing)jList1.getSelectedValue());
        DBConnection.showingId = s.getId();
        DBConnection.roomId = s.getRoomId();
        DBConnection.movieId = s.getMovieId();
        ComboBoxModel model = jComboBox1.getModel();
            Room r;
            for(int i = 0; i < model.getSize(); i++){
                r = (Room)model.getElementAt(i);
                if(r.getId().compareTo(DBConnection.roomId) == 0){
                    jComboBox1.setSelectedItem(r);
                }
            }
        model = jComboBox2.getModel();
            Movie m;
            for(int i = 0; i < model.getSize(); i++){
                m = (Movie)model.getElementAt(i);
                if(m.getId().compareTo(DBConnection.movieId) == 0){
                    jComboBox2.setSelectedItem(m);
                }
            }
        jTextField1.setText(s.getTime());
        jTextField2.setText(Float.toString(s.getPrice()));
    }//GEN-LAST:event_jButton6ActionPerformed


