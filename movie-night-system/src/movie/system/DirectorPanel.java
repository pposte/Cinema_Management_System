    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(jComboBox1.getSelectedItem() == null) return;
        DBConnection.movieId = ((Movie)jComboBox1.getSelectedItem()).getId();
        DBConnection.personId = DBConnection.getDirector();
        if(DBConnection.personId != null){
            ComboBoxModel model = jComboBox2.getModel();
            Person p;
            for(int i = 0; i < model.getSize(); i++){
                p = (Person)model.getElementAt(i);
                if(p.getId().compareTo(DBConnection.personId) == 0){
                    jComboBox2.setSelectedItem(p);
                }
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

