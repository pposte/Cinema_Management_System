    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int status = 0;
        if(jComboBox1.getSelectedItem() == null || jComboBox2.getSelectedItem() == null){
            return;
        }
        try {
        status = DBConnection.addDirector();
        } catch (Exception e){
            e.printStackTrace();
        }
        if(status > 0){
            JOptionPane.showMessageDialog(jComboBox1, "Director added!");
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't add director!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

