    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String title, genre, writer, actor, director;
        Movie[] mTitle, mGenre, mWriter, mActor, mDirector, movies;
        mTitle = mGenre = mWriter = mActor = mDirector = movies = null;
        Set<Movie> sTitle, sGenre, sWriter, sActor, sDirector, s;
        title = jTextField1.getText();
        director = jTextField2.getText();
        writer = jTextField3.getText();
        actor = jTextField4.getText();
        genre = jTextField5.getText();
        if(title.trim().compareTo("") != 0) mTitle = DBConnection.searchForMovies(title);
        if(director.trim().compareTo("") != 0) mDirector = DBConnection.searchForMoviesDirector(director);
        if(writer.trim().compareTo("") != 0) mWriter = DBConnection.searchForMoviesWriter(writer);
        if(actor.trim().compareTo("") != 0) mActor = DBConnection.searchForMoviesActor(actor);
        if(genre.trim().compareTo("") != 0) mGenre = DBConnection.searchForMoviesGenre(genre);
        
        s = new HashSet<>();
        if(mTitle != null){
            sTitle = new HashSet<>(Arrays.asList(mTitle));
            if(sTitle.size() > 0) s = sTitle;
        }
        if(mDirector != null){
            sDirector = new HashSet<>(Arrays.asList(mDirector));
            if(s.size() > 0 && sDirector.size() > 0) s.retainAll(sDirector);
            else if(s.isEmpty() && sDirector.size() > 0) s = sDirector;
        }
        if(mWriter != null){
            sWriter = new HashSet<>(Arrays.asList(mWriter));
            if(s.size() > 0 && sWriter.size() > 0) s.retainAll(sWriter);
            else if(s.isEmpty() && sWriter.size() > 0) s = sWriter;
        }
        if(mActor != null){
            sActor = new HashSet<>(Arrays.asList(mActor));
            if(s.size() > 0 && sActor.size() > 0) s.retainAll(sActor);
            else if(s.isEmpty() && sActor.size() > 0) s = sActor;
        }
        if(mGenre != null){
            sGenre = new HashSet<>(Arrays.asList(mGenre));
            if(s.size() > 0 && sGenre.size() > 0) s.retainAll(sGenre);
            else if(s.isEmpty() && sGenre.size() > 0) s = sGenre;
        }
        
        movies = s.toArray(new Movie[s.size()]);
        search(movies);
        CardLayout cl = (CardLayout)(search.getLayout());
        cl.show(search,"search");
    }//GEN-LAST:event_jButton1ActionPerformed

