    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int status = 0;
        if(jComboBox1.getSelectedItem() == null || jComboBox2.getSelectedItem() == null || jTextField1.getText().trim().compareTo("") == 0){
            return;
        }
        int index = jComboBox2.getSelectedIndex();
        try {
            status = DBConnection.editActor(jTextField1.getText());
        } catch (Exception e){
            e.printStackTrace();
        }
        if(status > 0){
            JOptionPane.showMessageDialog(jComboBox1, "Actor edited!");
            jComboBox2.setModel(new javax.swing.DefaultComboBoxModel(DBConnection.getActors()));
            jComboBox2.setSelectedIndex(index);
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't edit actor!");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

