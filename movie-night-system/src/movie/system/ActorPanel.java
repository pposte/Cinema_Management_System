    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int status = 0;
        if(jComboBox1.getSelectedItem() == null || jComboBox3.getSelectedItem() == null || jTextField2.getText().trim().compareTo("") == 0){
            return;
        }
        try {
            status = DBConnection.addActor(jTextField2.getText());
        } catch (Exception e){
            e.printStackTrace();
        }
        if(status > 0){
            JOptionPane.showMessageDialog(jComboBox1, "Actor added!");
            jComboBox2.setModel(new javax.swing.DefaultComboBoxModel(DBConnection.getActors()));
            jComboBox2.setSelectedIndex(-1);
            jTextField1.setText("");
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't add actor!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

