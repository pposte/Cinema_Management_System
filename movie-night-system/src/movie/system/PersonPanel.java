    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        String name = ((Person)jComboBox1.getSelectedItem()).getName();
        int result = 0;
        result = DBConnection.deletePerson();
        if(result > 0){
            JOptionPane.showMessageDialog(jComboBox1, name + " deleted!");
            jFormattedTextField1.setText("");
            jRadioButton1.setSelected(false);
            jRadioButton2.setSelected(false);
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(DBConnection.getPersons()));
            jComboBox1.setSelectedIndex(-1);
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't delete " + name +"!");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int status = 0;
        String name = ((JTextField)jComboBox1.getEditor().getEditorComponent()).getText();
        SimpleDateFormat formatter = new SimpleDateFormat("yyyy/MM/dd");
        java.util.Date javaDate = null;
        java.sql.Date date = null;
        try{
        javaDate = formatter.parse(jFormattedTextField1.getText());
        date = new java.sql.Date(javaDate.getTime());
        } catch (Exception e){
            e.printStackTrace();
        }
        
        Boolean sex = null;
        if(jRadioButton1.isSelected()){
            sex = true;//male
        }
        if(jRadioButton2.isSelected()){
            sex = false;//female
        }
        
        if(name == null || date == null || sex == null){
            return;
        }
        try {
        status = DBConnection.addPerson(name,date,sex);
        } catch (Exception e){
            e.printStackTrace();
        }
        if(status > 0){
            jComboBox1.setModel(new javax.swing.DefaultComboBoxModel(DBConnection.getPersons()));
            JOptionPane.showMessageDialog(jComboBox1, name + " added!");
        } else{
            JOptionPane.showMessageDialog(jComboBox1, "Couldn't add " + name +"!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

