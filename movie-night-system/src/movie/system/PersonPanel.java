    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        if(jComboBox1.getSelectedItem() == null) return;
        try{
            if(jComboBox1.getSelectedItem() instanceof String){
                return;
            }
        } catch(Exception e){ }
        String id = ((Person)jComboBox1.getSelectedItem()).getId();
        String[] fields = DBConnection.getPerson(id);
        DBConnection.personId = fields[0];
        String date = fields[2].split("\\s+")[0];
        date = date.replaceAll("-", "/");
        jFormattedTextField1.setText(date);
        if(fields[3].compareTo("1") == 0){
            jRadioButton1.setSelected(true);
        }else{
            jRadioButton2.setSelected(true);
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

